    public void logar(){
    
        String usuario = txtUsuario.getText();
        
        char[] senha = txtSenha.getPassword();
        
        String senhaReal = Arrays.toString(senha).replaceAll("\\[|\\]|,|\\s", "");
        
        try {
            
            AdmController ac = new AdmController();
            
            AdmBean ab = ac.controllerAdmLogar(usuario, senhaReal);
            
            if(ab.getAdm_id() == 0){
            
                frmFilho = new frmDiaUsuSenhaInvalido(this, true);
                
                frmFilho.setVisible(true);
                
                txtSenha.setText("");
                
                txtUsuario.requestFocus();
            
            }else{
            
                frmHome frm = new frmHome();
                frm.setVisible(true);
                this.dispose();
            
            }
            
        } catch (ClassNotFoundException | HeadlessException e) {
            System.out.println(e.getMessage());
        }
    
    }

