    public void buscaCf(){
    
        String cpf = txtCpf.getText();
        
        if(cpf.length()==11){
        
            try {
                
                EleitorController ec = new EleitorController();
                
                int valida = ec.controllerValidaCpf(cpf);
                
                if(valida == 0){
                
                    frmFilhoValidaCpf = new frmDiaCpfInvalido(null, true);
                    frmFilhoValidaCpf.setVisible(true);
                    
                    txtCpf.setText("");
                    
                }else{
                    
                    CandidatoController cc = new CandidatoController();
                    
                    int eleicao = cc.controllerRetornaId();
                    if(eleicao > 0){
                        int id = ec.controllerRetornaId(cpf);

                        int validaVoto = ec.controllerValidaVoto(eleicao, id);

                        if(validaVoto == 1){

                            frmFilhoJaVotou = new frmDiaJaVotou(null, true);

                            frmFilhoJaVotou.setVisible(true);

                            destruir();

                        }else{

                            frmVotacao frm = new frmVotacao();
                            frm.setVisible(true);
                            frm.recebeId(id, eleicao);
                            frmPai.destruirTela();
                            this.dispose();

                        }
                    }else{
                    
                        txtCpf.setText("");
                    
                    }
                }
                
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        
        }
    
    }

